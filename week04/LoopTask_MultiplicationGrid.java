package week04;

public class LoopTask_MultiplicationGrid {

	public static void main(String[] args) {
		
		for (int i = 1 ; i <= 1 ; i++) {
			for (int j = 1; j <= 9 ; j++) {
				for (int m = 1; m <= 9 ; m++) {
					int g = j * m;
					System.out.printf("%2d " , g);
				}System.out.println();
				
				
			} 
		} 
	}

}
