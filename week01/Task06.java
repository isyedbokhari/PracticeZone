package week01;

public class Task06 {

	public static void main(String[] args) {
//	    Write a Java program to print the sum (addition), multiply,
//		subtract, divide and remainder of two numbers.
		
		int num1 = 10;
		int num2 = 2;
		
		System.out.println("num1 + num2 = " + (num1 + num2));
		System.out.println("num1 - num2 = " + (num1 - num2));
		System.out.println("num1 * num2 = " + (num1 * num2));
		System.out.println("num1 / num2 = " + (num1 / num2));
		System.out.println("num1 % num2 = " + (num1 % num2));

	}

}
